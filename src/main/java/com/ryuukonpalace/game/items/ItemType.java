package com.ryuukonpalace.game.items;

/**
 * Types d'objets disponibles dans le jeu.
 */
public enum ItemType {
    CONSUMABLE,     // Objets consommables (potions, nourriture, etc.)
    EQUIPMENT,      // Équipements (armures, accessoires, etc.)
    CAPTURE_STONE,  // Pierres de capture pour les variants
    KEY_ITEM,       // Objets clés pour les quêtes
    MATERIAL,       // Matériaux pour l'artisanat
    MYSTICAL_SIGN,  // Signes mystiques (sorts, capacités)
    QUEST_ITEM,     // Objets de quête
    TREASURE,       // Trésors et objets de valeur
    MISC            // Objets divers
}
